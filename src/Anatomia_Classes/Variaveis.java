package Anatomia_Classes;

public class Variaveis {
	public static void main(String[] args) {
		
		
		//Criando uma variável que não poderá ter seu valor modificado
		
		//String -> tipo de variável que representa texto
		final String br = "Brasil";
		
		//double -> representa números reais
		final double pi = 3.1415;
		
		
		
		//int -> representa números inteiros
		int numero;
		//declarando o valor da variável
		numero = 25;
		//mostrando o valor desse numero
		System.out.println(numero);
		
		
		
		
	}
	
}
